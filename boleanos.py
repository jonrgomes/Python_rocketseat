False

# Condicionias
# if = see condição
# else = se não
if True:
    print("Bloco IF vai ser executado")
else:
    print("Bloco ELSE não será executada")

# Operadores logicos: and e or

# AND
if True and True:
    print("Bloco será executado")

if True and False:
    print("Bloco não será executado")

# OR
if True or False:
    print("Bloco OR vai ser executado")
if False or False:
    print("Bloco OR não vai ser executado")
if True or True:
    print("Bloco OR vai ser executado")